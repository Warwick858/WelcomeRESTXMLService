using System.ServiceModel;
using System.ServiceModel.Web;

namespace WelcomeRESTXMLService
{
    [ServiceContract]
    public interface IWelcomeRESTXMLService
    {
        [OperationContract]
        [WebGet(UriTemplate = "/welcome/{yourName}")] // WebGet Attribute returns data in XML
        string Welcome(string yourName);
    } // end interface IWelcomeRESTXMLService
} // end namespace WelcomeRESTXMLService
